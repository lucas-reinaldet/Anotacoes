# Locais importantes de arquivos Nginx
# Por padrão, o Nginx armazena diferentes arquivos de configuração e log nos seguintes locais:

# /var/www/html – Conteúdo do site visto pelos visitantes.
# /etc/nginx  – Localização dos principais arquivos do aplicativo Nginx.
# /etc/nginx/nginx.conf –  O arquivo de configuração principal do Nginx.
# /etc/nginx/sites-available – Lista de todos os sites configurados através do Nginx.
# /etc/nginx/sites-enabled –  Lista de sites ativamente servidos pelo Nginx.
# /var/log/nginx/access.log – Logs de acesso que rastreiam todas as solicitações ao seu servidor.
# /var/log/ngins/error . log  – Um log de quaisquer erros gerados no Nginx.